export const ALL_HUNT_MISSIONS = Object.values(HUNT_MISSIONS).flat()

/**
 * Filter missions for a specific hunt session.
 * @param {Object} opts
 * @param {boolean} opts.together - Whether both partners are together right now
 * @param {'new'|'established'|'rooted'} opts.stage - Relationship stage
 * @param {string} opts.timeTag - Time available: 'short', 'medium', 'any'
 * @param {string[]} opts.excludeKeys - Mission keys already used by this couple
 */
export function getAvailableMissions({ together, stage, timeTag, excludeKeys = [] }) {
  return ALL_HUNT_MISSIONS.filter(m => {
    if (excludeKeys.includes(m.key)) return false
    if (!together && m.together_required) return false
    if (m.stage !== 'any' && m.stage !== stage) return false
    if (timeTag && timeTag !== 'any' && m.time_tag !== 'any' && m.time_tag !== timeTag) return false
    return true
  })
}
