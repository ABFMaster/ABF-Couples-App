export function getBetQuestion({ coupleAgeDays, usedIds = [] }) {
  function buildPool(ignoreUsed) {
    let pool = BET_QUESTIONS
    if (!ignoreUsed) {
      pool = pool.filter(q => !usedIds.includes(q.id))
    }
    return pool
  }

  const pool = buildPool(false).length > 0 ? buildPool(false) : buildPool(true)

  // Build weighted pool: level 1 weighted heavily early, level 2-3 unlock at 30+ days
  const weighted = []
  for (const q of pool) {
    let weight = 0
    if (coupleAgeDays < 14) {
      weight = q.level === 1 ? 1 : 0
    } else if (coupleAgeDays < 30) {
      weight = q.level === 1 ? 4 : q.level === 2 ? 1 : 0
    } else {
      weight = q.level === 1 ? 3 : q.level === 2 ? 2 : 1
    }
    for (let i = 0; i < weight; i++) weighted.push(q)
  }

  // Fallback: if weighted pool is empty (e.g. all level 1 used and couple < 14 days), use full pool
  const finalPool = weighted.length > 0 ? weighted : pool

  return finalPool[Math.floor(Math.random() * finalPool.length)]
}
